class Solution {
  public:
    int romanToDecimal(string s) {
        unordered_map<char, int> roman = {
            {'I', 1},
            {'V', 5},
            {'X', 10},
            {'L', 50},
            {'C', 100},
            {'D', 500},
            {'M', 1000},
        };
        
        int ans = 0;
        int x = 0;
        
        for(int i = s.length() - 1;i>=0;i--){
            if(roman[s[i]] >= x){
                
                ans+=roman[s[i]];
                x = roman[s[i]];
            }
            else{
                ans-=roman[s[i]];
            }
        }
        return ans;
    }
};

